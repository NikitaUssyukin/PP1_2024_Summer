#include <iostream>

using namespace std;

/*
variables

they hold some data of a defined type

fundamental data types
- int, used for whole numbers (1, 2, 3, 0, -1, 213, etc)
- double, used for decimal numbers (3.14, 1.01, 0.00001, -29.64, etc)
- char, used for characters ('a', 'A', '0', '!', ' ', '\n', etc)
- bool, used for storing boolen valuse, that is true (1) or false (0)
*/

int main() {
    char a;

    a = 'A';

    cout << a << endl;

    bool b;

    b = 0;

    cout << b << endl;

    b = true;

    cout << b << endl;

    return 0;
}