#include <iostream>

using namespace std;

// relational operators
int main() {
    
    int a, b;

    cin >> a >> b;

    // equal to - ==
    cout << "a == b: " << (a == b) << endl;

    // not equal to - !=
    cout << "a != b: " << (a != b)<< endl;

    // more than - >
    cout << "a > b : " << (a > b) << endl;

    // less than - <
    cout << "a < b : " << (a < b) << endl;

    // more than or equal to - >=
    cout << "a >= b : " << (a >= b) << endl;

    // less than or equal to - <=
    cout << "a <= b : " << (a <= b) << endl;

    return 0;
}

