#include <iostream>

using namespace std;

/*
arithmetic operations

+
-
*
/
%

*/

int main() {
    int a, b;
    
    cin >> a >> b;

    cout << "a + b: " << a + b << endl;

    cout << "a - b: " << a - b << endl;

    cout << "a * b: " << a * b << endl;

    cout << "a / b: " << a / b << endl;

    cout << "a % b: " << a % b << endl;

    return 0;
}