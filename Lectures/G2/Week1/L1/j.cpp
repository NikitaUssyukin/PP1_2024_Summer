#include <iostream>
#include <cmath>

using namespace std;

/*
mathematical operations (functions)

privided by the <cmath> library
*/

int main() {
    int a;

    cin >> a;

    cout << "sqrt(a): " << sqrt(a) << endl;

    cout << "pow(a, 2): " << pow(a, 2) << endl;

    cout << "pow(2, a): " << pow(2, a) << endl;

    cout << "pow(a, 3): " << pow(a, 3) << endl;


    return 0;
}